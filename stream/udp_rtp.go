func (h *StreamHub) UpdateInterfaces(udpAddr string, ifaces []string) error {
	h.Mu.Lock()
	defer h.Mu.Unlock()

	addr, err := net.ResolveUDPAddr("udp", udpAddr)
	if err != nil {
		return err
	}

	var newConn *net.UDPConn
	if len(ifaces) == 0 {
		newConn, err = net.ListenMulticastUDP("udp", nil, addr)
		if err != nil {
			newConn, err = net.ListenUDP("udp", addr)
			if err != nil {
				return err
			}
		}
	} else {
		var lastErr error
		for _, name := range ifaces {
			iface, ierr := net.InterfaceByName(name)
			if ierr != nil {
				lastErr = ierr
				continue
			}
			newConn, err = net.ListenMulticastUDP("udp", iface, addr)
			if err == nil {
				break
			}
			lastErr = err
		}
		if newConn == nil {
			newConn, err = net.ListenUDP("udp", addr)
			if err != nil {
				return fmt.Errorf("ÊâÄÊúâÁΩëÂç°ÁõëÂê¨Â§±Ë¥•‰∏î UDP ÁõëÂê¨Â§±Ë¥•: %v (last=%v)", err, lastErr)
			}
		}
	}

	_ = newConn.SetReadBuffer(8 * 1024 * 1024)

	if h.UdpConn != nil {
		_ = h.UdpConn.Close()
	}
	h.UdpConn = newConn
	h.addr = udpAddr

	logger.LogPrintf("UDP ÁõëÂê¨Âú∞ÂùÄÊõ¥Êñ∞Ôºö%s ifaces=%v", udpAddr, ifaces)
	return nil
}

