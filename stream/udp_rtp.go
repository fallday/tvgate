// ====================
// ÂàõÂª∫Êñ∞Hub
// ====================
func NewStreamHub(udpAddr string, ifaces []string) (*StreamHub, error) {
	addr, err := net.ResolveUDPAddr("udp", udpAddr)
	if err != nil {
		return nil, err
	}
	if addr == nil || addr.IP == nil || !isMulticast(addr.IP) {
		return nil, fmt.Errorf("‰ªÖÊîØÊåÅÂ§öÊí≠Âú∞ÂùÄ: %s", udpAddr)
	}

	var conn *net.UDPConn
	var lastErr error

	if len(ifaces) == 0 {
		// Êó†ÊåáÂÆöÁΩëÂç°Êó∂ÔºåÂ∞ùËØïÊâÄÊúâÊé•Âè£
		conn, err = listenMulticast(addr, nil)
		if err != nil {
			return nil, err
		}
	} else {
		// ÈÅçÂéÜÊåáÂÆöÊé•Âè£
		for _, name := range ifaces {
			iface, ierr := net.InterfaceByName(name)
			if ierr != nil {
				lastErr = ierr
				continue
			}
			conn, err = listenMulticast(addr, iface)
			if err == nil {
				break
			}
			lastErr = err
		}
		if conn == nil {
			return nil, fmt.Errorf("ÊâÄÊúâÁΩëÂç°ÁõëÂê¨Â§±Ë¥•: %v", lastErr)
		}
	}

	_ = conn.SetReadBuffer(8 * 1024 * 1024)

	hub := &StreamHub{
		Clients:     make(map[chan []byte]struct{}),
		AddCh:       make(chan chan []byte, 1024),
		RemoveCh:    make(chan chan []byte, 1024),
		UdpConn:     conn,
		Closed:      make(chan struct{}),
		BufPool:     &sync.Pool{New: func() any { return make([]byte, 32*1024) }},
		CacheBuffer: NewRingBuffer(4096),
		addr:        udpAddr,
	}

	go hub.run()
	go hub.readLoop()

	return hub, nil
}

// Â∞ÅË£ÖË∑®Âπ≥Âè∞Â§öÊí≠ÁõëÂê¨
func listenMulticast(addr *net.UDPAddr, iface *net.Interface) (*net.UDPConn, error) {
	conn, err := net.ListenUDP("udp", addr)
	if err != nil {
		return nil, fmt.Errorf("ListenUDP failed: %w", err)
	}

	// ËÆæÁΩÆ socket ÈÄâÈ°π (Ë∑®Âπ≥Âè∞)
	if raw, err := conn.SyscallConn(); err == nil {
		raw.Control(func(fd uintptr) {
			setReuse(fd)
		})
	}

	// Âä†ÂÖ•Â§öÊí≠ÁªÑ
	p := ipv4.NewPacketConn(conn)
	if iface != nil {
		if err := p.JoinGroup(iface, addr); err != nil {
			conn.Close()
			return nil, fmt.Errorf("JoinGroup failed: %w", err)
		}
	}

	return conn, nil
}

// ====================
// ÂÆ¢Êà∑Á´ØÁÆ°ÁêÜÂæ™ÁéØ
// ====================
func (h *StreamHub) run() {
	ticker := time.NewTicker(30 * time.Second)
	defer ticker.Stop()

	for {
		select {
		case <-ticker.C:
			h.checkIsolation()
		case ch := <-h.AddCh:
			h.Mu.Lock()
			h.Clients[ch] = struct{}{}
			go h.sendInitial(ch)
			h.Mu.Unlock()
		case ch := <-h.RemoveCh:
			h.Mu.Lock()
			if _, ok := h.Clients[ch]; ok {
				delete(h.Clients, ch)
				close(ch)
			}
			if len(h.Clients) == 0 {
				h.Mu.Unlock()
				h.Close()
				return
			}
			h.Mu.Unlock()
		case <-h.Closed:
			h.Mu.Lock()
			for ch := range h.Clients {
				close(ch)
			}
			h.Clients = nil
			h.Mu.Unlock()
			return
		}
	}
}

func (h *StreamHub) checkIsolation() {
	h.Mu.Lock()
	defer h.Mu.Unlock()
	for _, hub := range GlobalMultiChannelHub.Hubs {
		if hub != h && hub.UdpConn == h.UdpConn {
			logger.LogPrintf("CRITICAL: Ê£ÄÊµãÂà∞ËøûÊé•ÂÖ±‰∫´! %s ‰∏é %s", h.addr, hub.addr)
		}
	}
}

// ====================
// ËØªÂèñUDPÂπ∂ÂàÜÂèë
// ====================
func (h *StreamHub) readLoop() {
	defer func() {
		if r := recover(); r != nil {
			logger.LogPrintf("‚ùå readLoop recovered from panic: %v", r)
		}
	}()

	for {
		// Ê£ÄÊü•ÊòØÂê¶Â∑≤ÁªèÂÖ≥Èó≠
		select {
		case <-h.Closed:
			return
		default:
		}

		if h.UdpConn == nil {
			logger.LogPrintf("‚ö†Ô∏è readLoop exit: UdpConn is nil")
			return
		}

		buf := h.BufPool.Get().([]byte)
		n, _, err := h.UdpConn.ReadFromUDP(buf)
		if err != nil {
			h.BufPool.Put(buf)
			select {
			case <-h.Closed:
				return
			default:
				continue
			}
		}

		// Êã∑Ë¥ùÊï∞ÊçÆ
		data := make([]byte, n)
		copy(data, buf[:n])
		h.BufPool.Put(buf)

		// ÂÜôÂÖ•HubÁä∂ÊÄÅ
		h.Mu.Lock()
		if h.CacheBuffer == nil {
			h.Mu.Unlock()
			return
		}

		h.PacketCount++
		h.LastFrame = data
		h.CacheBuffer.Push(data)

		// Ëá™Âä®Êé¢ÊµãÊ†ºÂºè
		if h.DetectedFormat == "" {
			h.DetectedFormat = detectStreamFormat(data)
		}

		// Ê£ÄÊµãÂÖ≥ÈîÆÂ∏ß
		if h.DetectedFormat != "" && h.isKeyFrameByFormat(data, h.DetectedFormat) {
			// ‰øùÂ≠ò LastKeyFrame
			h.LastKeyFrame = data

			// ‰øùÂ≠òÂÆåÊï¥ÂàùÂßãÂåñÂ∏ß: ÊúÄËøëËã•Âπ≤ÁºìÂ≠ò + ÂΩìÂâçÂÖ≥ÈîÆÂ∏ß
			cached := h.CacheBuffer.GetAll()
			h.LastInitFrame = append(h.LastInitFrame[:0], cached...) // ‰øùÁïôÁºìÂ≠òÈáåÂÖ≥ÈîÆÂ∏ßÂâçÁöÑSPS/PPS
		}

		// Êã∑Ë¥ùÂÆ¢Êà∑Á´ØÂàóË°®
		clients := make([]chan []byte, 0, len(h.Clients))
		for ch := range h.Clients {
			clients = append(clients, ch)
		}
		h.Mu.Unlock()

		// ÊäïÈÄíÊï∞ÊçÆÂà∞ÂÆ¢Êà∑Á´Ø
		for _, ch := range clients {
			select {
			case ch <- data:
			default:
				h.Mu.Lock()
				h.DropCount++
				h.Mu.Unlock()
			}
		}
	}
}
// ====================
// Êñ∞ÂÆ¢Êà∑Á´ØÂèëÈÄÅÂÆåÊï¥ÂàùÂßãÂåñÂ∏ß + ÂêéÁª≠Â∏ß
// ====================
func (h *StreamHub) sendInitial(ch chan []byte) {
	h.Mu.Lock()
	defer h.Mu.Unlock()

	// ÂèëÈÄÅÂÆåÊï¥ÂàùÂßãÂåñÂ∏ß (SPS/PPS + IDR)
	if len(h.LastInitFrame) > 0 {
		for _, f := range h.LastInitFrame {
			select {
			case ch <- f:
			default:
			}
		}
		return
	}

	// ÁºìÂ≠ò‰∏≠Êü•ÊâæÊúÄËøëÁöÑÂÖ≥ÈîÆÂ∏ß
	var sentKey bool
	for _, f := range h.CacheBuffer.GetAll() {
		if !sentKey && h.isKeyFrameByFormat(f, h.DetectedFormat) {
			sentKey = true
		}
		if sentKey {
			select {
			case ch <- f:
			default:
			}
		}
	}
}

